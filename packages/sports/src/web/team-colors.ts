// Null means "no brand color known" — the score bar renders its neutral ink fallback.
export function teamBarColor(competitionKey: string, teamKey: string): TeamBarColor | null {
  const entry = TEAM_BAR_COLORS[`${competitionKey}:${teamKey}`];
  if (!entry) return null;
  return typeof entry === "string" ? { bg: entry, fg: WHITE } : entry;
}
